export const FallingAsciiAnimation = () => {
  const [currentFrame, setCurrentFrame] = useState(0);
  const fontSize = "8px";

  useEffect(() => {
    const interval = setInterval(() => {
      setCurrentFrame((prevFrame) => (prevFrame + 1) % asciiFrames.length);
    }, 600);

    return () => clearInterval(interval);
  }, []);

  return (
    <div className="w-full h-full flex items-center justify-center overflow-hidden">
      <pre
        className="text-[#00FF00] bg-[#000000 ] leading-[1.1] font-mono whitespace-pre overflow-hidden text-center"
        style={{ fontSize: fontSize }}
      >
        {asciiFrames[currentFrame]}
      </pre>
    </div>
  );
};

export default FallingAsciiAnimation;
